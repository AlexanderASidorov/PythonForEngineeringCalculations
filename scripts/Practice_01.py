"""
Задача #10. 
Сумма простых чисел меньше 10: 2 + 3 + 5 +7 = 17.
Найдите сумму простых чисел меньше 1000.
"""

print ('----Task #2-------')
maximum_integer = 100 # максимальное натуральное числе
print ('Простые числа меньше ' + str(maximum_integer) + ':')

i=1 # переменная, которая в цикле будем меняться от 1 до 1000 с шагом 1
sum_=0 # переменная к которой мы будем прибавлять число i, если докажем, что
        # число i - простое число 

while i<maximum_integer: # цикл продолжается до тех пор, пока i меньше 1000
    i+=1 # шаг изменения переменной i
    count=1 # мнимая переменная. если = 1, значит числь простое, если нет, то
        # имеет делители отличные от 1 и самой себя
    for j in range(2,i): # цикл от 2 до числа i с шагом 1
        if i%j==0: # проверяем делится ли число i  на число j
            count=0 # если хоть один раз, услвовие i%j==0, значит число i
                    # не является простым и переменную count мы обнуляем и
                    # далее число i не будет добавлятся к переменной sum_
            break
    
    if count==1: # переменная count==1 если условие i%j==0 ни разу не было выполнено
                    # т.е. число i - простое
        sum_ += i
        print(i, end=' ')
        
print(' ')
print('Сумма этих чисел = '  + str(sum_))
print('-----------------')







