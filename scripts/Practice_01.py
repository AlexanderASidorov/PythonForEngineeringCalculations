"""
Задача #1. 

Если мы возьмем все натуральные числа меньше 10, которые делятся на 3 и 5, то 
то получится следующее: 3, 5, 6 и 9. Их сумма равна 23.
Найти сумму натуральных чисел до 1000, которые делятся на 5 и 7.
"""

sum_integers = 0 # переменная куда мы будем накапливать сумму

integers = range(1, 1001) # числа [1, 2, ..., 10]
first_number = 5 # первый делитель
second_number = 7 # второй делитель

for number in integers: # проверяем каждое число в списке integers
    # проверяем делится ли число на 5 или 7 
  if number % first_number == 0 or number % second_number == 0:
      # если делится, то добавляем его к общей сумме
      sum_integers  += number
print ('----Task #1-------')
print ('Максимальное целое число ' + str(max(integers)))
print('Первый делитель ' + str(first_number))
print('Второй делитель ' + str(second_number))
print('Сумма всех чисел, которые делятся на ' 
      + str(first_number) + ' и на '  + str (second_number) 
      + ' = ' + str(sum_integers))
print('-----------------')
print(' ')



